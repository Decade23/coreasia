const getByPath = (source: Record<string, any>, path: string) => {
  return path.split('.').reduce<any>((acc, part) => acc?.[part], source)
}

export const useConsoleI18n = () => {
  const { locale } = useCoreI18n()

  const consoleLocale = computed<ConsoleLocale>(() => (locale.value === 'en' ? 'en' : 'id'))
  const dictionary = computed(() => consoleMessages[consoleLocale.value])

  const tc = (path: string, params?: Record<string, string | number>) => {
    const fallback = getByPath(consoleMessages.id as unknown as Record<string, any>, path)
    const raw = getByPath(dictionary.value as unknown as Record<string, any>, path) ?? fallback ?? path

    if (typeof raw !== 'string') return raw
    if (!params) return raw

    return raw.replace(/\{\{(.*?)\}\}/g, (_, key) => String(params[key.trim()] ?? ''))
  }

  const dateLocale = computed(() => (consoleLocale.value === 'en' ? 'en-US' : 'id-ID'))

  return {
    locale: consoleLocale,
    dateLocale,
    tc,
  }
}
